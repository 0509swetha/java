for (let i=1; i<=7; i++){
    if(i==1){
        console.log("monday");
    }else if(i==2){
        console.log("tuesday");
    }else if(i==3){
        console.log("wednesday");
    }else if(i==4){
        console.log("thursday");
    }else if(i==5){
        console.log("friday");
    }else if(i==6){
        console.log("saturday");
    }else if(i==7){
        console.log("sunday");
    }
}

